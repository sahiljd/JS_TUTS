let a=20
let b=12

//Arithmetic Operators
console.log("a+b ",a + b)
console.log("a-b ",a - b)
console.log("a*b",a * b)
console.log("a/b ",a / b)
console.log("a%b ",a % b)
console.log("a**b ",a**b)

//Unary
console.log(a++);
console.log(++a);
console.log(a--);
console.log(--a);

//Assignment

console.log("a+=b ",a += b)
console.log("a-=b ",a -= b)
console.log("a*=b",a *= b)
console.log("a/=b ",a /=b)
console.log("a%=b ",a %=b)
console.log("a**=b ",a**=b)

//Comparison

console.log("a>b ",a > b)
console.log("a<b ",a < b)
console.log("a>=b",a >= b)
console.log("a<=b ",a <= b)
console.log("a!=b ",a != b)
console.log("a==b ",a==b)


